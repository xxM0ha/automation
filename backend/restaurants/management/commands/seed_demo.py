class Command(BaseCommand):
    help = 'Seed Laffe restaurant with real menu data.'

    @transaction.atomic
    def handle(self, *args, **kwargs):
        from restaurants.models import User

        # ── Restaurant ────────────────────────────────────────────────────────
        restaurant, _ = Restaurant.objects.get_or_create(
            slug='laffe',
            defaults={'name': 'لافي', 'is_active': True},
        )

        # ── Admin user ────────────────────────────────────────────────────────
        admin, created = User.objects.get_or_create(
            email='admin@laffe.com',
            defaults={'first_name': 'Admin', 'is_staff': True},
        )
        if created:
            admin.set_password('admin123')
            admin.save()
        RestaurantUser.objects.get_or_create(
            restaurant=restaurant, user=admin,
            defaults={'role': 'admin'},
        )

        # ── Kitchen user ──────────────────────────────────────────────────────
        kitchen, created = User.objects.get_or_create(
            email='kitchen@laffe.com',
            defaults={'first_name': 'Kitchen'},
        )
        if created:
            kitchen.set_password('kitchen123')
            kitchen.save()
        RestaurantUser.objects.get_or_create(
            restaurant=restaurant, user=kitchen,
            defaults={'role': 'kitchen'},
        )

        # ── Connect all platforms ─────────────────────────────────────────────
        all_platforms = list(Platform.objects.all())
        for platform in all_platforms:
            RestaurantPlatform.objects.get_or_create(
                restaurant=restaurant,
                platform=platform,
                defaults={'is_connected': True, 'auto_accept': False, 'menu_sync': True},
            )

        # ── Wipe old mock menu items for this restaurant ──────────────────────
        MenuItem.objects.filter(restaurant=restaurant).delete()
        MenuCategory.objects.filter(restaurant=restaurant).delete()

        # ── Seed categories and items ─────────────────────────────────────────
        created_count = 0
        for cat_name, sort_order, item_names in CATEGORIES:
            category = MenuCategory.objects.create(
                restaurant=restaurant,
                name_ar=cat_name,
                sort_order=sort_order,
            )
            for entry in item_names:
                if isinstance(entry, tuple):
                    item_name, item_price = entry
                else:
                    item_name, item_price = entry, 0
                item = MenuItem.objects.create(
                    restaurant=restaurant,
                    category=category,
                    name=item_name,
                    price=item_price,
                )
                # Link to all platforms; set known platform names
                _names = {
                    # زراعي names fill gaps; Riz/Zahour names take precedence
                    'toters':  TOTERS_NAMES.get(item_name) or TOTERS_ZIRA3I_NAMES.get(item_name, ''),
                    'tiptop':  TIPTOP_NAMES.get(item_name, ''),
                    'lezzo':   LEZZO_NAMES.get(item_name, ''),
                    'yammak':  YAMMAK_NAMES.get(item_name, ''),
                }
                for platform in all_platforms:
                    MenuItemPlatform.objects.create(
                        menu_item=item,
                        platform=platform,
                        platform_name=_names.get(platform.slug, ''),
                    )
                created_count += 1

        self.stdout.write(self.style.SUCCESS(
            f'Seeded {created_count} menu items across {len(CATEGORIES)} categories '
            f'for restaurant=laffe.\n'
            f'admin=admin@laffe.com / admin123\n'
            f'kitchen=kitchen@laffe.com / kitchen123'
        ))
